#include "libft.h"

size_t	ft_strlcpy(char *dst, char *src, size_t size)
{
	unsigned int	i;
	unsigned int	src_length;

	i = 0;
	src_length = ft_strlen(src);
	if (size == 0)
		return (src_length);
	while (i < (size - 1) && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (src_length);
}
